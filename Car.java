package com.hillel.prokopenko.javaelementary;

public class Car {
    int id;
    int year;
    int price;
    String brand;
    String model;
    String registrationNumber;
    String color;

    public Car(int id, int year, int price, String brand, String model, String registrationNumber, String color) {
        this.id = id;
        this.year = year;
        this.price = price;
        this.brand = brand;
        this.model = model;
        this.registrationNumber = registrationNumber;
        this.color = color;
    }
}
